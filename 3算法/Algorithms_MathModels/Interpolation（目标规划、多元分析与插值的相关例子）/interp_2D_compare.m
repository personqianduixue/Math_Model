figure(1);
meshz(x,y,z)
xlabel('X'),ylabel('Y'),zlabel('Z')

xi=0:50:5600;
yi=0:50:4800;

figure(2)
z1i=interp2(x,y,z,xi,yi','nearest');
surfc(xi,yi,z1i)
xlabel('X'),ylabel('Y'),zlabel('Z')

figure(3)
z2i=interp2(x,y,z,xi,yi');
surfc(xi,yi,z2i)
xlabel('X'),ylabel('Y'),zlabel('Z')

figure(4)
z3i=interp2(x,y,z,xi,yi','cubic');
